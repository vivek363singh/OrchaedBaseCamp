package com.mindtree.museum;

 class Visitors {

	
		private String name,gender; int age,fees;



		public Visitors(String name, String gender, int age) {
			super();
			this.name = name;
			this.gender = gender;
			this.age = age;
		}
		
		



		public void setFees(int fees) {
			this.fees = fees;
		}





		public String getName() {
			return name;
		}



		public String getGender() {
			return gender;
		}



		public int getAge() {
			return age;
		}
		
		

	}


