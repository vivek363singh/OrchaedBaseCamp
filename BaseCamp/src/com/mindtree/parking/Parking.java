package com.mindtree.parking;

class Parking {
	
	private String vechicle_no,time;
	
	

	public Parking(String vechicle_no, String time) {
		
		this.vechicle_no = vechicle_no;
		this.time = time;
	}

	public String getVechicle_no() {
		return vechicle_no;
	}

	public String getTime() {
		return time;
	}
	
	

}
