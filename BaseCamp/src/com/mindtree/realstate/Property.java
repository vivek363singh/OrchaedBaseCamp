package com.mindtree.realstate;

class Property {
	
	String name,type1,type2,size;

	public Property(String name, String type1, String type2, String size) {
		super();
		this.name = name;
		this.type1 = type1;
		this.type2 = type2;
		this.size = size;
	}

	public String getName() {
		return name;
	}

	public String getType1() {
		return type1;
	}

	public String getType2() {
		return type2;
	}

	public String getSize() {
		return size;
	}
	
	

}
